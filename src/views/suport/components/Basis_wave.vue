				<p class="topZero">外国对本国商品产生需求的同时，本国货币的需求量和外国货币的供应量通常也会相应增加。本国货币需求量的增加可使本国货币升值，而外国货币供应量的增加会使外国货币贬值。它的原理如下。</p>
