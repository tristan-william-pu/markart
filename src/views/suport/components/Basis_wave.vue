				<h3>四、政府干预和货币操纵</h3>
				<p class="topZero">政府干预和货币操纵指的是政府通过其中央银行在外汇市场中采取的特定措施，使其货币价值上升或下降的行为。所谓的特定措施分为“直接干预”和“口头干预”两种。直接干预是指政府通过在公开市场买卖货币的方式操纵货币价值。口头干预是指政府单纯的口头警告，实质上不干预市场交易。在参与本国货币买卖时，政府理所当然地有很大的可能性去操纵货币。但是有时候仅靠警告就足以达到预期目标，政府根本无需投入大量资金。</p>
				<p>直接干预和口头干预有两种形式：协调干预和单边干预。协调干预是指多国政府以直接干预或口头干预的形式进行市场干预。单边干预是指一国政府单独参与货币操纵。</p>
