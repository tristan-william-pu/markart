				<h3>避险货币</h3>
				<p class="topZero">避险货币可以在投资者恐慌肆虐时繁荣。“避险货币”之所以得名是因为它们能在金融动荡时提供庇护。避险货币包括美元(USD)、瑞士法郎(CHF)和日元(JPY)。</p>
